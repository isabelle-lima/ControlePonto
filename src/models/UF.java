package models;

public class UF {

	private int id;
	private String uf;
	private String nome_uf;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	public String getNome_uf() {
		return nome_uf;
	}
	public void setNome_uf(String nome_uf) {
		this.nome_uf = nome_uf;
	}
	
}
